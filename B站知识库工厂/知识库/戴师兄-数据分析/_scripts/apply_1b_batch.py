def count_body_chars(text: str) -> int:
    """Count Chinese chars + alphanumeric in body (exclude markdown syntax roughly)."""
    # Strip code blocks for count
    t = re.sub(r"```[\s\S]*?```", "", text)
    t = re.sub(r"[#*`\[\]|>\-\n\r\s]", "", t)
    return len(t)


def update_file(name: str, body: str) -> int:
    path = BASE / name
    content = path.read_text(encoding="utf-8")
    wc = count_body_chars(body)

    # Update frontmatter
    content = re.sub(r"处理日期: .*", f"处理日期: {DATE}", content)
    content = re.sub(r"阶段: .*", f"阶段: 01B_文章正文完成", content)
    if "正文字数:" in content:
        content = re.sub(r"正文字数: .*", f"正文字数: {wc}", content)
    else:
        content = re.sub(
            r"(阶段: 01B_文章正文完成\n)",
            rf"\1正文字数: {wc}\n",
            content,
        )

    # Remove existing 文章正文 if any
    if "## 文章正文" in content:
        content = content.split("## 文章正文")[0].rstrip()

    # Ensure separator before body
    if not content.rstrip().endswith("---"):
        content = content.rstrip() + "\n\n---\n"

    content = content.rstrip() + f"\n\n## 文章正文\n\n{body.strip()}\n"
    path.write_text(content, encoding="utf-8")
    return wc


