def append_body(filename, word_count, body):
    path = BASE / filename
    if not path.exists():
        print(f"MISSING {filename}")
        return
    text = path.read_text(encoding="utf-8")
    if "## 文章正文" in text:
        print(f"SKIP {filename} (has body)")
        return
    # find last line of structured section - append before end
    if not text.rstrip().endswith("---"):
        # find last --- before we'd add
        pass
    # update frontmatter
    text = re.sub(
        r"处理日期: \d{4}-\d{2}-\d{2}\n阶段: [^\n]+\n",
        f"处理日期: 2026-07-19\n阶段: 01B_文章正文完成\n正文字数: {word_count}\n",
        text,
        count=1,
    )
    text = text.rstrip() + "\n" + body + "\n"
    path.write_text(text, encoding="utf-8")
    print(f"OK {filename} ({word_count})")

